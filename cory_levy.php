			<div class="accordian-inner speaker_bio">Cory Levy is a 20 year-old entrepreneur with over five years of startup experience. At the age of 15, he worked for NutShell Mail (acquired by Constant Contact). From there he went on to do externships, while in high school, at TechStars, Union Square Ventures, DFJ Mercury, and the Founders Fund. Additionally, Levy created the NextGen Conference, a conference bringing together young entrepreneurs with more experienced entrepreneurs and VCs. Previous speakers include: Peter Thiel (Founder of PayPal), Scooter Braun (Justin Bieber’s manager), and Ashton Kutcher (attendee). As a result of Levy’s work, he has been asked to speak about entrepreneurship to university students across the country including Duke University and the University of Illinois.

Levy studied Computer Science at the University of Illinois. At the University of Illinois, Levy, as a freshman, was a Teachers Assistant in the Engineering Entrepreneurship course. He received four merit scholarships from the Dean of Engineering. Levy spends his time at One working in operations, technical management, and business development. He is taking a leave of absence to work full-time on One.</div>
