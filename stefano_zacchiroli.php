		<div id="zacchiroli_talk_abstract" class="accordion-body collapse">
      			<div class="accordion-inner talk_abstract">Debian is one of the oldest Free and Open Source Software (FOSS) distributions in existence. The project was founded in 1993 to further Free Software distribution, and is still doing so in a purely community-based way. The Debian Project and distribution are both made by volunteers who employ a dual "do-ocratic" (a form of meritocracy based on the outcome of individual work) and democratic model to make decisions and drive Debian toward the goal of creating the best-possible entirely Free Software operating system. The uniqueness of Debian is manifest in its Free Software values, independence from commercial interests, and in its importance as the base of a huge ecosystem of several hundreds of derived distributions, which include today's most popular GNU/Linux distributions.

In this talk, Zacchiroli will present the Debian Project and distribution, discuss its unique traits, and outline the communication challenges that a distribution entirely based on "geek" communities has to face.</div>
