      			<div class="accordion-inner talk_abstract">A revolution in education is coming; recently massive online courses have brought together millions of students from all over the world. These courses have been designed from the ground-up as complete classes, where students are expected to submit homework, meet deadlines, and are awarded a "Statement of Accomplishment" only upon meeting a high grading bar. This talk will be about the key technological ideas that Coursera has developed to support massive online courses. Ngiam will discuss interactive video lectures, scalable online Q&A forums, sophisticated auto-graded homeworks, and carefully-designed peer grading pipelines that leverage the crowd for grading open-ended essays and design work at scale.</div>
