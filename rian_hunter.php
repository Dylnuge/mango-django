      			<div class="accordion-inner talk_abstract">Dropbox aims to propagate changes across your computers as fast as possible. In most cases, as soon as a file is changed on your phone or on the web or another computer, the new data is usually available on all your computers within seconds. At the core of this system is a small number of highly-efficient and custom-built servers, each responsible for notifying millions of computers when they should be syncing with Dropbox. In this talk, you’ll hear about how Dropbox engineered this system, pushed the Linux networking stack to its absolute limit to blow the C10K problem out of the water, and came to support tens of millions of connected users with roughly a dozen servers.</div>
