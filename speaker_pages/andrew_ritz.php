		</div>
		<div id="ritz_talk_abstract" class="accordion-body collapse">
      			<div class="accordion-inner talk_abstract">The advent of System of Chip (SOC) PCs has generated a number of new opportunities for Windows. SOC PCs promise a tightly integrated system and unique new peripheral devices. SOC PCs introduce new usage models for consumers, and offer a new level of energy efficiency and battery life. To address this opportunity, Microsoft had to understand how an SOC PC could be best designed. In this talk, Ritz will review some of the key hardware components of SOC PCs and how Windows was optimized for these components. He will review how Windows evolved to support new usage models, and how battery life was optimized for SOC PCs.</div>
