			<div class="accordian-inner speaker_bio">Andrew Ritz is the development manager for core networking in Windows, which provides the heart of secure, simple, and scalable communications for mobile devices, enterprise networks, and the cloud. Ritz helped enable network communications for Windows 8, and helped redefine the networking for the private/public/hybrid cloud through Hyper-V Network Virtualization and Hyper-V Extensible switch. A 16 year Microsoft veteran, Ritz was previously the development manager for the Windows Kernel, working on a number of server scalability projects, innovating on power management for mobile devices, and helping provide the abstraction layer between core platform hardware and Windows. Ritz holds a BS in Computer Engineering from the University of Illinois. In his spare time, he enjoys running, reading, following sports, and spending time with his family.</div>
